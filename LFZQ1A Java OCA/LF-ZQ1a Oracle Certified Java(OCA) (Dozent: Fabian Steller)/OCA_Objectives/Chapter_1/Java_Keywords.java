package chapter_1;

// Es folgt eine Auflistung aller Keywords in Java 8
// abstract 	- bei der Deklaration von Klassen oder Methoden
// assert		- wirft eine Exception wenn die folgende Bedingung false ist (Debugging)
// boolean		- primitiver Datentyp
// break		- bricht eine Schleife oder ein switch-case ab
// byte			- primitiver Datentyp
// case			- zur Definition von cases im switch-case
// catch		- zum Auffangen von Exceptions, folgt auf ein try
// char			- primitiver Datentyp
// class		- zur Deklaration von Klassen
// const		- in Java ohne Bedeutung!
// continue		- springt zur nächsten Iteration der Schleife
// default		- zur Definition des default cases im switch-case oder für nicht-abstrakte Methoden in Interfaces
// do			- leitet eine Fußgesteuerte Schleife ein, darauf folgt ein while
// double		- primitiver Datentyp
// else			- wird ausgeführt, wenn das vorangehende if false evaluiert wird
// enum			- zur Deklaration eines Enums
// extends  	- zur Vererbung zwischen Klassen oder Interfaces
// false		- unwahr
// final		- zur Deklaration von Konstanten, zur Deklaration von Klassen von denen nicht geerbt werden darf, 
//				zur Deklaration von Methoden die nicht überschrieben werden dürfen
// finally		- wird immer ausgeführt, folgt auf ein catch oder direkt auf ein try
// float		- primitiver Datentyp
// for      	- zur Deklaration von Schleifen
// goto			- in Java ohne Bedeutung!
// if			- leitet einen konditionalen Block ein
// implements	- zur Vererbung von Interfaces zu Klassen
// import		- zum Importieren von Klassen
// instanceof	- zum Überprüfen der Klassenzugehörigkeit einer Instanz
// int			- primitiver Datentyp
// interface	- zur Deklaration eines Interfaces
// long			- primitiver Datentyp
// native		- zur Deklaration von Methoden, welche nicht in Java implementiert sind, nicht im OCA
// new			- zum Instanziieren von Klassen, ruft einen Konstruktor auf
// null			- möglicher Wert für Referenzvariablen (null bedeutet soviel wie "fehlende Information")
// package		- um Dateien Packages zuzuordnen
// private		- Access-Modifier für Methoden und nicht-lokale Variablen
// protected	- Access-Modifier für Methoden und nicht-lokale Variablen
// public		- Access-Modifier für Klassen, Methoden und nicht-lokale Variablen
// return		- Bricht eine Methode ab und gibt den evtl. nachfolgenden Wert zurück
// short		- primitver Datentyp
// static		- macht Methoden oder nicht-lokale Variablen instanzunabhängig
// strictfp		- für Variablen - definiert einen Standard zur Behandlung von Nachkommastellen, nicht im OCA
// super		- Verweis auf die Elternklasse
// switch		- leitet switch-case ein
// synchronized	- für Methoden, thread-safety, nicht im OCA
// this			- Verweis auf die aktuell ausführende Instanz
// throw		- wirft eine Exception
// throws		- Deklariert, dass eine Methode eine Exception werfen könnte
// transient	- markiert Variablen, die nicht serialisiert werden sollen - nicht im OCA
// true			- wahr
// try			- leitet einen "geschützten Bereich" ein (zum Auffangen von Exceptions)
// void			- für Methoden ohne Rückgabe
// volatile		- markiert Variablen, so dass sie nicht aus dem main memory für threads kopiert werden - nicht im OCA
// while		- leitet Kopfgesteuerte Schleife ein
